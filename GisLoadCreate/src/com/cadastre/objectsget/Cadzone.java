package com.cadastre.objectsget;

public class Cadzone {
	private String cadzone;
	private String oficii;
	
	public Cadzone() {
		super();
	}
	
	public Cadzone(String cadzone, String oficii) {
		super();
		this.cadzone = cadzone;
		this.oficii = oficii;
	}

	public String getCadzone() {
		return cadzone;
	}
	public void setCadzone(String cadzone) {
		this.cadzone = cadzone;
	}
	public String getOficii() {
		return oficii;
	}
	public void setOficii(String oficii) {
		this.oficii = oficii;
	}
	
	
}
